package com.naumstore.controller;

public interface DefaultResponseTag {

    /**
     This tag used when we transfer a user.
     */
    String USER = "user";

    /**
     This tag used when we transfer a list of users.
     */
    String USERS = "users";

    /**
     This tag used when we transfer a product.
     */
    String PRODUCT = "product";

    /**
     This tag used when we transfer a list of products.
     */
    String PRODUCTS = "products";

    /**
     This tag used when we transfer an order.
     */
    String ORDER = "order";

    /**
     This tag used when we transfer another result.
     */
    String RESULT = "result";
}
